for i in range( 0 , 4 ) :
    sum +=리스트[i]

print(" 리스트내 누적합계 : " , sum )

print(" 리스트내 마지막 인덱스 삭제 : " , 리스트.pop() )
print(" 리스트 확인 : " , 리스트 )

리스트.sort()
print(" 리스트내 정렬후 : " ,  리스트 )

리스트.reverse()
