# 딕셔너리 = 사전
    # { 키 : 데이터  , 키 : 데이터 , 키 : 데이터  , 키 : 데이터  }
    # 키를 이용한 데이터 호출
    #   키는 중복이 불가능하다 .. [ 중복일경우 앞전 키의 데이터 변경 ]
    #   데이터는 중복 가능
딕셔너리 = { }

딕셔너리["이름"] = "유재석"      # 딕셔너리명[ 키 ] = 데이터
