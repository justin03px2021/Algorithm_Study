리스트.insert( 2 , 100 )
print(" 리스트내 2번 인덱스 추가 : " ,  리스트 )

리스트.remove(100)
print(" 리스트내 100 데이터 삭제 : " , 리스트 )

리스트.extend( [10 , 20 , 30 ] )
print(" 리스트 연결(상속) : " , 리스트 )

print(" 리스트내 길이 : " , len(리스트) )
print(" 리스트내 10 데이터 개수 : " , 리스트.count(10) )

# 2차원 리스트 [ 가로 / 세로 ]
    # [   [ 1행 ]  ,  [  2행  ]  ,  [  3행  ]  ]
    # [   [ 리스트1 ]  ,  [  리스트1  ]  ,  [  리스트1  ]  ]
리스트1 = [ ]
리스트2 = [ ]
