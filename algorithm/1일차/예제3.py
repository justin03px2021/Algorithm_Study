# 변수 : 하나의 데이터만 저장
# 리스트 : 여러개의 데이터 저장
    # [  ] 사용
    # 인덱스 = 저장되는 순서 번호가 존재 ~~ [ 0번 시작 ]
    # 리스트 자주 사용되는 함수
        # 1. .append( ) : 리스트내 가장 뒤에 데이터 추가
            # . insert( 인덱스 , 데이터 ) 해당 인덱스에 데이터 삽입
        # 2. .pop() : 리스트내 가장 뒤에 데이터 삭제
            # .remove( 데이터 ) : 해당 데이터 삭제
        # 3. .sort() : 리스트내 데이터 오름차순
        # 4. .reverse() : 리스트내 데이터 내림차순
        # 5. .extend( 리스트 ) : 두개 이상의 리스트를 연결
        # 6. .count( 데이터 ) : 해당 데이터의 개수
        # 7. len( 리스트 ) : 해당 리스트내 데이터 전체 개수

        # *. 리스트명[인덱스번호] = 데이터

리스트 = [ ]   # 리스트 선언

for i in range( 0 , 4 ) :
    리스트.append( 0 )

for i in range( 0 , 4 ) :
    리스트[i] = int( input( str(i+1) +"번째 입력 : ") )
print( 리스트 )

